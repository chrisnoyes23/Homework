public class operators
{
        public static void main(String[] args)

        {
            int x = 5;
            int y = 2;
            System.out.printf("%d+%d=%d\n" , x , y , x+y );
            System.out.printf("%d-%d=%d\n" , x , y , x-y );
            System.out.printf("%dx%d=%d\n" , x , y , x*y );
            System.out.printf("%d/%d=%d\n" , x , y , x/y );
        }
}